package com.rodrigoleite.fundamentosjava.aula75_84;

public class Aula81 {

    public static void main(String[] args) {
        
        String teste = "Teste";

        /*Método: toLowerCase(): transforma todos os caracteres de um string para minúsculas */

        System.out.println(teste); // Teste
        System.out.println(teste.toLowerCase()); // teste

        /*Método toUpperCase(): transforma todos os caracteres de uma string para maiúsculas */

        System.out.println(teste.toUpperCase()); // TESTE

    }
}
